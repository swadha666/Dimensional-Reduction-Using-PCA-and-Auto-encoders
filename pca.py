# Step 5: VQ-VAE Simulation (Improved)
# -----------------------------
from sklearn.cluster import KMeans
import numpy as np
import matplotlib.pyplot as plt

# 1. Get latent vectors for a subset of the dataset
x_latent_subset = encoder_model.predict(x_train[:10000])

# 2. Apply KMeans for vector quantization
n_clusters = 32  # more clusters for diversity
kmeans = KMeans(n_clusters=n_clusters, random_state=42)
kmeans.fit(x_latent_subset)

# 3. Quantize latent vectors (replace each point with its cluster center)
quantized_latent = kmeans.cluster_centers_[kmeans.labels_]

# 4. Add small random noise to each cluster center to simulate variation
noise_scale = 0.05  # small perturbation
quantized_latent_noisy = quantized_latent[:10] + np.random.normal(0, noise_scale, (10,2))

# 5. Decode quantized latent vectors with noise
decoded_quantized_imgs = generator_model.predict(quantized_latent_noisy)

